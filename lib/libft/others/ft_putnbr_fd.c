#include "libft.h"

void	ft_putnbr_fd(long long n, int fd)
{
	char	c_nb;

	if (n == -9223372036854775807)
	{
		write(fd, "-9223372036854775807", 20);
		return ;
	}
	else
	{
		if (n < 0)
		{
			n *= -1;
			write(fd, "-", 1);
		}
		if (n > 9)
		{
			ft_putnbr_fd(n / 10, fd);
		}
		c_nb = n % 10 + '0';
		write(fd, &c_nb, 1);
	}
}
