#include "libft.h"

size_t	ft_nblen(long nb)
{
	int	i;

	i = 0;
	if (nb < 0)
	{
		nb *= -1;
		i++;
	}
	if (!nb)
		i++;
	while (nb)
	{
		nb /= 10;
		i++;
	}
	return (i);
}
